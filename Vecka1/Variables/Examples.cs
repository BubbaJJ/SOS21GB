namespace Vecka1.Variables
{
    class Variables
    {
        public static void Examples()
        {
            // Deklarera en integer (1, 34, 564)
            int i;

            // Deklarera en string ("Hej", "B", "SOS21")
            string str;

            // Deklarera en boolean (true / false)
            bool b;

            // Deklarera en character ('A', 'a', '2')
            char c;

            // Deklarera en double (3.14, 5.7, 45.964)
            double d;

            // Deklara och tilldela en variabel
            int x = 34;

            // Tilldela en variabel
            x = 25;
        }
    }
